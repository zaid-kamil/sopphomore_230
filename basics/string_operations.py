# string operations

# concatenation
a = 'apple'
b = 'juice'
ab = a + b 
print(ab)

w1 = 'this'
w2 = 'is'
w3 = 'Amazing'
msg = w1 + w2 + w3
print(msg) 
# adding space b/w string
msg = w1 + ' ' + w2 + ' ' + w3
print(msg)
msg = f'{w1} {w2} {w3}'
print(msg)

# string multiplication
word = 'Hi'
print(word * 3)
print('-' * 25)
